    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String v = jfullname.getText();
        String a = jemail.getText();
        String n = jtxt.getText();
        String i = jdate.getText();
        try {
            mongo = new MongoClient("localhost", 27017);
            dbconnection = mongo.getDatabase("BigData");
            collection = dbconnection.getCollection("Client_message");

            Document collecting = new Document();

            if (v.equals("")) {
                JOptionPane.showMessageDialog(null, "Add your Fullname!");
            } else if (a.equals("")) {
                JOptionPane.showMessageDialog(null, "Enter Email!");
            } else if (n.equals("")) {
                JOptionPane.showMessageDialog(null, "Enter your message!");
            }else {
                collecting.append("Fullname", jfullname.getText());
                 collecting.append("Username", namecontact.getText());
                collecting.append("Email", jemail.getText());
                collecting.append("Message", jtxt.getText());
                collecting.append("Date", jdate.getText());
                collection.insertOne(collecting);
                JOptionPane.showMessageDialog(null, "Your message has been sent!");
                jfullname.setText("");
                jemail.setText("");
                jtxt.setText("");

            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1MouseClicked

